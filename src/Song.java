public class Song {
    //variables
    private String artist;
    private String title;

    //constructors
    public Song(){

    }

    //methods
    public String getArtist() {
        return artist;
    }

    public void setArtist(String artist) {
        this.artist = artist;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
